package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {

         if(true){ // if statement
             System.out.println("Wooden Spoon"); //1 time write
         }

        System.out.println("----------------------------------------");
         while (true){ //  repeated if
            System.out.println("Wooden Spoon"); // multiple  times write
        }


    }
}
