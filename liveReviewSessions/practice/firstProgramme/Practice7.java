public class Practice7 {
    public static void main(String[] args) {
        int a = 30;
        System.out.println(++a);
        System.out.println(++a);

        int b= 40;
        System.out.println(--b);
        System.out.println(--b);

        int c= 50;
        System.out.println(c++);
        System.out.println(c++);
        System.out.println(c);

        int d= 60;
        System.out.println(d--);
        System.out.println(d--);
        System.out.println(d);


    }
}
